class Main2 {
    public static void main(String args[]){
        //unary operators
        int a=10;
        a++;//11
        System.out.println(a);
        ++a;//12
        System.out.println(a);
        --a;//11
        System.out.println(a);
        a--;//10
        System.out.println(a);
        //logiccal
        System.out.println(5>3 && 2>1);
        //shift
        System.out.println((6<<1) +"  "+ (6>>1));
    }
}
